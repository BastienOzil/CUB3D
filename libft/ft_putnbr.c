#include "libft.h"

int	ft_putnbr(int n)
{
	int		c;
	long	nb;

	c = 0;
	nb = n;
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
		c++;
	}
	if (nb >= 10)
		c += ft_putnbr(nb / 10);
	ft_putchar(nb % 10 + '0');
	c++;
	return (c);
}
